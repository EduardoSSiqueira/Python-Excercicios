#Crie uma função que tenha 3 parâmetros: um número inteiro, um número real e um texto. 
#A função deve imprimir os valores dos parâmetros na tela. 
#Em seguida, chame a função passando os argumentos de forma posicional.

def imprimir_valores(inteiro, real, texto):
    print("Número inteiro:", inteiro)
    print("Número real:", real)
    print("Texto:", texto)

# Chamando a função passando os argumentos de forma posicional
imprimir_valores(54, 3.54, "Exercicio 5.3!")
